export function getBusinessById(id) {
  return businesses.find((b) => b.id === Number(id))
}

export function getBusinessBySlug(slug) {
  return businesses.find((b) => b.slug === slug)
}

export function getFeaturedBusinesses() {
  return businesses.filter((b) => b.featured)
}

export function getBusinessSummary(business) {
  if (!business) return null
  return {
    id: business.id,
    slug: business.slug,
    nameKey: business.nameKey,
    logo: business.logo,
    tier: business.tier,
    verified: business.verified,
    rating: business.rating,
  }
}
